const century3Data = [];

function addYear(year, dynasty, emperor, title, category, desc, isMajor = false) {
  const entry = {
    year,
    dynasty,
    emperor,
    events: [
      {
        title,
        category,
        desc
      }
    ]
  };
  if (isMajor) {
    entry.events[0].isMajor = true;
  }
  century3Data.push(entry);
}

function addEvent(year, title, category, desc) {
  const entry = century3Data.find(e => e.year === year);
  if (entry) {
    entry.events.push({
      title,
      category,
      desc
    });
  }
}

