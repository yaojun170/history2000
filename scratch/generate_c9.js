import fs from 'fs';
import path from 'path';

const century9Data = [];

function addYear(year, dynasty, emperor, title, category, desc, isMajor = false) {
  const entry = {
    year,
    dynasty,
    emperor,
    events: [
      {
        title,
        category,
        desc
      }
    ]
  };
  if (isMajor) {
    entry.events[0].isMajor = true;
  }
  century9Data.push(entry);
}

function addEvent(year, title, category, desc) {
  const entry = century9Data.find(e => e.year === year);
  if (entry) {
    entry.events.push({
      title,
      category,
      desc
    });
  }
}

