const century13Data = [];

function addYear(year, dynasty, emperor, title, category, desc, isMajor = false) {
  const entry = {
    year,
    dynasty,
    emperor,
    events: [
      {
        title,
        category,
        desc
      }
    ]
  };
  if (isMajor) {
    entry.events[0].isMajor = true;
  }
  century13Data.push(entry);
}

function addEvent(year, title, category, desc, isMajor = false) {
  const entry = century13Data.find(e => e.year === year);
  if (entry) {
    const newEvent = {
      title,
      category,
      desc
    };
    if (isMajor) {
      newEvent.isMajor = true;
    }
    entry.events.push(newEvent);
  }
}

